% reformat Y %

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

function edata= reformatY(Y1,Y2,Y3,Y4)

% sizes
n = size(Y1,1);
t = 4;
q=size(Y1,2);

% structs
edata = [];

% phenotypes
edata.Y = zeros(n*t,q);
for j=1:n
    ind=(j-1)*t+(1:t);
    edata.Y(ind,:)=[Y1(j,:);Y2(j,:);Y3(j,:);Y4(j,:)];
end

end

